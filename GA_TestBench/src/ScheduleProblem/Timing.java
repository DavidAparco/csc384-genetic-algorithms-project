package ScheduleProblem;

/**
 *
 * @author Dave
 */
public class Timing {

    private int day;
    private int time;

    public Timing(int day, int time) {
        this.day = day;
        this.time = time;
    }

    public int getDay() {
        return day;
    }

    public int getTime() {
        return time;
    }
}
